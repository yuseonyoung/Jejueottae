package kr.or.ddit.middle.vo;

public class MileageVO {

	private String mil_code;
	private String mem_id;
	private String mil_his;
	private int mil_amt;
	private int mil_total;
	private String mil_date;

	public String getMil_code() {
		return mil_code;
	}

	public void setMil_code(String mil_code) {
		this.mil_code = mil_code;
	}

	public String getMem_id() {
		return mem_id;
	}

	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}

	public String getMil_his() {
		return mil_his;
	}

	public void setMil_his(String mil_his) {
		this.mil_his = mil_his;
	}

	public int getMil_amt() {
		return mil_amt;
	}

	public void setMil_amt(int mil_amt) {
		this.mil_amt = mil_amt;
	}

	public int getMil_total() {
		return mil_total;
	}

	public void setMil_total(int mil_total) {
		this.mil_total = mil_total;
	}

	public String getMil_date() {
		return mil_date;
	}

	public void setMil_date(String mil_date) {
		this.mil_date = mil_date;
	}

}
